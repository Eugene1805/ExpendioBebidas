    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        Cliente clienteSeleccionado = (Cliente) cbCliente.getSelectedItem();
        if (clienteSeleccionado != null) {
            ReporteClienteNoVendidoController.buscarProductosNoVendidos(tbProductosNoVendidos, clienteSeleccionado.getIdCliente());
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un cliente v√°lido.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnBuscarClienteActionPerformed

    private void btnExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarActionPerformed
        Cliente clienteSeleccionado = (Cliente) cbCliente.getSelectedItem();
        if (clienteSeleccionado != null) {
            String[] encabezados = {
                "Cliente: " + clienteSeleccionado.getRazonSocialCliente()
            };

            PDFExporter.exportarTabla(
                tbProductosNoVendidos,
                "Productos no vendidos a un cliente",
                encabezados,
                "productos_no_vendidos_" + clienteSeleccionado.getIdCliente() + ".pdf",
                this
            );
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un cliente para exportar el reporte.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnExportarActionPerformed

